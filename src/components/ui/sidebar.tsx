const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContext = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContext | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)

    // This is the internal state of the sidebar.
    // We use openProp and setOpenProp for control from outside the component.
    const [_open, _setOpen] = React.useState(defaultOpen)
    // Hover state: when hovering the sidebar area on desktop we show it temporarily
    const [hoverOpen, setHoverOpen] = React.useState(false)
    // Timer ref to debounce mouse leave so sidebar doesn't disappear immediately
    const leaveTimerRef = React.useRef<number | null>(null)
    // Cleanup timer on unmount
    React.useEffect(() => {
      return () => {
        if (leaveTimerRef.current) {
          window.clearTimeout(leaveTimerRef.current)
          leaveTimerRef.current = null
        }
      }
    }, [])

    // Track pointer movements to determine if the cursor is over the sidebar element itself.
    React.useEffect(() => {
      if (isMobile) return

      // Determine which side the sidebar is on (default left)
      let sidebarSide: "left" | "right" = "left"
      try {
        const sidebarEl = document.querySelector('[data-sidebar="sidebar"]') as HTMLElement | null
        const sideEl = sidebarEl?.closest("[data-side]") as HTMLElement | null
        const attr = sideEl?.getAttribute("data-side")
        if (attr === "right") sidebarSide = "right"
      } catch {
        // ignore
      }

      const EDGE_THRESHOLD = 32

      const onMove = (e: MouseEvent) => {
        const target = e.target as Element | null
        const overSidebar = target?.closest('[data-sidebar="sidebar"]') != null

        if (overSidebar) {
          if (leaveTimerRef.current) {
            window.clearTimeout(leaveTimerRef.current)
            leaveTimerRef.current = null
          }
          setHoverOpen(true)
          return
        }

        // Open when cursor is near the configured edge
        const cx = e.clientX
        const atLeftEdge = cx <= EDGE_THRESHOLD
        const atRightEdge = cx >= window.innerWidth - EDGE_THRESHOLD

        if ((sidebarSide === "left" && atLeftEdge) || (sidebarSide === "right" && atRightEdge)) {
          if (leaveTimerRef.current) {
            window.clearTimeout(leaveTimerRef.current)
            leaveTimerRef.current = null
          }
          setHoverOpen(true)
          return
        }

        if (leaveTimerRef.current) {
          window.clearTimeout(leaveTimerRef.current)
        }
        // Hide immediately when cursor leaves sidebar/edge trigger area
        leaveTimerRef.current = window.setTimeout(() => {
          setHoverOpen(false)
          leaveTimerRef.current = null
        }, 0)
      }

      const onOut = (e: MouseEvent) => {
        // If relatedTarget is null, the pointer left the window.
        if (!e.relatedTarget) {
          if (leaveTimerRef.current) {
            window.clearTimeout(leaveTimerRef.current)
            leaveTimerRef.current = null
          }
          setHoverOpen(false)
        }
      }

      document.addEventListener("mousemove", onMove)
      window.addEventListener("mouseout", onOut)

      return () => {
        document.removeEventListener("mousemove", onMove)
        window.removeEventListener("mouseout", onOut)
        if (leaveTimerRef.current) {
          window.clearTimeout(leaveTimerRef.current)
          leaveTimerRef.current = null
        }
      }
    }, [isMobile])
    const open = openProp ?? (_open || hoverOpen)
    const setOpen = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const openState = typeof value === "function" ? value(open) : value
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }

        // This sets the cookie to keep the sidebar state.
        document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
      },
      [setOpenProp, open]
    )

    // Helper to toggle the sidebar.
    const toggleSidebar = React.useCallback(() => {
      return isMobile
        ? setOpenMobile((open) => !open)
        : setOpen((open) => !open)
    }, [isMobile, setOpen, setOpenMobile])

    // Adds a keyboard shortcut to toggle the sidebar.
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    // We add a state so that we can do data-state="expanded" or "collapsed".
    // This makes it easier to style the sidebar with Tailwind classes.
    const state = open ? "expanded" : "collapsed"

    const contextValue = React.useMemo<SidebarContext>(
      () => ({
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
    )

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            className={cn(
              "sidebar-root group/sidebar-wrapper flex min-h-svh w-full has-[[data-variant=inset]]:bg-transparent",
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    side?: "left" | "right"
    variant?: "sidebar" | "floating" | "inset"
    collapsible?: "offcanvas" | "icon" | "none"
  }
>(
  (
    {
      side = "left",
      variant = "sidebar",
      collapsible = "offcanvas",
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (collapsible === "none") {
      return (
        <div
          className={cn(
            "flex h-full w-[--sidebar-width] flex-col bg-sidebar text-sidebar-foreground",
            className
          )}
          ref={ref}
          {...props}
        >
          {children}
        </div>
      )
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] bg-white/80 dark:bg-slate-950/80 backdrop-blur-xl p-0 text-sidebar-foreground [&>button]:hidden"
            side={side}
          >
            <SheetTitle className="sr-only">Navegação</SheetTitle>
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      )
    }

    return (
      <div
        ref={ref}
        className="group peer hidden lg:block text-sidebar-foreground"
        data-state={state}
        data-collapsible={state === "collapsed" ? collapsible : ""}
        data-variant={variant}
        data-side={side}
      >
        {/* Spacer removed so sidebar overlays content instead of pushing it */}
        <div
          className={cn(
            "duration-200 relative h-svh w-0 bg-transparent transition-[width] ease-linear",
            "group-data-[side=right]:rotate-180",
            variant === "floating" || variant === "inset"
              ? "group-data-[collapsible=icon]:w-[calc(var(--sidebar-width-icon)_+_theme(spacing.4))]"
              : "group-data-[collapsible=icon]:w-[--sidebar-width-icon]"
          )}
        />
        <div
          className={cn(
            "duration-200 fixed inset-y-0 z-40 hidden h-svh w-[--sidebar-width] transition-[left,right,width,transform] ease-linear lg:flex",
            side === "left"
              ? "left-0 group-data-[collapsible=offcanvas]:-translate-x-[var(--sidebar-width)]"
              : "right-0 group-data-[collapsible=offcanvas]:translate-x-[var(--sidebar-width)]",
            // Adjust the padding for floating and inset variants.
            variant === "floating" || variant === "inset"
              ? "p-2 group-data-[collapsible=icon]:w-[calc(var(--sidebar-width-icon)_+_theme(spacing.4)_+2px)]"
              : "group-data-[collapsible=icon]:w-[--sidebar-width-icon] group-data-[side=left]:border-r group-data-[side=right]:border-l",
            "group-data-[collapsible=offcanvas]:pointer-events-none group-data-[collapsible=offcanvas]:opacity-0 group-data-[collapsible=offcanvas]:shadow-none",
            className
          )}
          {...props}
        >
          <div
            data-sidebar="sidebar"
            className="flex h-full w-full flex-col bg-transparent"
          >
            {children}
          </div>
        </div>
        {/* SidebarRail removido para não atrapalhar interação na borda */}
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

const SidebarTrigger = React.forwardRef<
  React.ElementRef<typeof Button>,
  React.ComponentProps<typeof Button>
>(({ className, onClick, ...props }, ref) => {
  const { toggleSidebar } = useSidebar()

  return (
    <Button
      ref={ref}
      data-sidebar="trigger"
      variant="ghost"
      size="icon"
      className={cn("h-11 w-11 rounded-lg border border-transparent hover:border-border", className)}
      onClick={(event) => {
        onClick?.(event)
        toggleSidebar()
      }}
      {...props}
    >
      <PanelLeft />
      <span className="sr-only">Toggle Sidebar</span>
    </Button>
  )
})
